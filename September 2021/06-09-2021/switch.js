var n1 = 10;
var n2 = 20;
var ch = 2;
switch(ch)
{
    case '1': console.log(n1+n2);
            break;
    case '2': console.log(n1-n2);
            break;
    case '3': console.log(n1*n2);
            break;
    case '4': console.log(n1/n2);
            break;
    default: console.log("Invalid Choice");
}